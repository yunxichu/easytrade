def set_cell_shading(cell, fill: str) -> None:
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def set_cell_text(cell, text: str, bold: bool = False, color: RGBColor | None = None) -> None:
    cell.text = ""
    paragraph = cell.paragraphs[0]
    paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = paragraph.add_run(text)
    run.bold = bold
    if color:
        run.font.color.rgb = color
    run.font.size = Pt(9)


def style_table(table, header_fill: str = "1F4E79") -> None:
    table.style = "Table Grid"
    for row_idx, row in enumerate(table.rows):
        for cell in row.cells:
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
            for paragraph in cell.paragraphs:
                paragraph.paragraph_format.space_after = Pt(0)
                for run in paragraph.runs:
                    run.font.name = "Microsoft YaHei"
                    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Microsoft YaHei")
                    run.font.size = Pt(9)
        if row_idx == 0:
            for cell in row.cells:
                set_cell_shading(cell, header_fill)
                for paragraph in cell.paragraphs:
                    for run in paragraph.runs:
                        run.bold = True
                        run.font.color.rgb = RGBColor(255, 255, 255)


def add_heading(document: Document, text: str, level: int) -> None:
    paragraph = document.add_heading(text, level=level)
    for run in paragraph.runs:
        run.font.name = "Microsoft YaHei"
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Microsoft YaHei")
        run.font.color.rgb = RGBColor(31, 78, 121)


def add_paragraph(document: Document, text: str, bold_lead: str | None = None) -> None:
    paragraph = document.add_paragraph()
    paragraph.paragraph_format.first_line_indent = Pt(18)
    paragraph.paragraph_format.line_spacing = 1.18
    paragraph.paragraph_format.space_after = Pt(6)
    if bold_lead and text.startswith(bold_lead):
        lead = paragraph.add_run(bold_lead)
        lead.bold = True
        paragraph.add_run(text[len(bold_lead) :])
    else:
        paragraph.add_run(text)
    for run in paragraph.runs:
        run.font.name = "Microsoft YaHei"
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Microsoft YaHei")
        run.font.size = Pt(10.5)


def add_bullet(document: Document, text: str) -> None:
    paragraph = document.add_paragraph(style="List Bullet")
    paragraph.paragraph_format.line_spacing = 1.12
    paragraph.paragraph_format.space_after = Pt(3)
    run = paragraph.add_run(text)
    run.font.name = "Microsoft YaHei"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Microsoft YaHei")
    run.font.size = Pt(10)


def pct(x: float) -> str:
    if pd.isna(x):
        return ""
    return f"{x:.2%}"


def num(x: float, digits: int = 4) -> str:
    if pd.isna(x):
        return ""
    return f"{x:.{digits}f}"


def short_symbol(symbol: str) -> str:
    return (
        str(symbol)
        .replace("OG-CALL-", "C")
        .replace("OG-PUT-", "P")
        .replace("ES-CALL-", "C")
        .replace("ES-PUT-", "P")
        .replace("-2020", "-")
    )


def vol_summary(stats: pd.DataFrame) -> dict[str, float]:
    return {
        "iv_min": float(stats["iv_min"].min()),
        "iv_max": float(stats["iv_max"].max()),
        "iv_median": float(stats["iv_median"].mean()),
        "rv_latest": float(stats["rv_latest"].mean()),
        "rv_median": float(stats["rv_median"].mean()),
        "rv_mean": float(stats["rv_mean"].mean()),
        "rv_min": float(stats["rv_min"].min()),
        "rv_max": float(stats["rv_max"].max()),
        "max_gap": float(stats["max_gap_minutes"].max()),
    }


def set_column_widths(table, widths_cm: list[float]) -> None:
    table.autofit = False
    for row in table.rows:
        for idx, width in enumerate(widths_cm):
            if idx < len(row.cells):
                row.cells[idx].width = Cm(width)


def build_result_table(document: Document, latest: pd.DataFrame) -> None:
    for _, row in latest.iterrows():
        text = (
            f"{short_symbol(row['option_symbol'])}：K={num(row['strike'], 0)}，"
            f"F={num(row['futures_price'], 2)}，期权价={num(row['option_mid'], 2)}，"
            f"IV={pct(row['iv'])}（分位 {pct(row['iv_percentile'])}），"
            f"RV={pct(row['rv'])}（分位 {pct(row['rv_percentile'])}），"
            f"Delta={num(row['delta'], 4)}，Gamma={num(row['gamma'], 5)}，Vega={num(row['vega'], 2)}。"
        )
        add_bullet(document, text)


def build_attr_table(document: Document, attr_totals: pd.DataFrame) -> None:
    for _, row in attr_totals.iterrows():
        text = (
            f"{short_symbol(row['option_symbol'])}：价格变化={num(row['price_change'], 4)}，"
            f"Delta贡献={num(row['delta_effect'], 4)}，Gamma贡献={num(row['gamma_effect'], 4)}，"
            f"Vega贡献={num(row['vega_effect'], 4)}，Theta贡献={num(row['theta_effect'], 4)}，"
            f"残差={num(row['residual'], 4)}。"
        )
        add_bullet(document, text)


